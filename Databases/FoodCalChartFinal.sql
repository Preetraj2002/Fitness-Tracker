CREATE OR REPLACE TRIGGER calculate_calories
BEFORE INSERT ON FOODLOG
FOR EACH ROW
DECLARE
    food_cal DECIMAL(10, 2);
BEGIN
    SELECT "CAL/100" INTO food_cal
    FROM FoodCalChart
    WHERE FOODNAME = :NEW.Food;

    :NEW.Calories := (:NEW.Quantity / 100) * food_cal;
END;
/
